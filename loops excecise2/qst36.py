x= int(input("enter side X : \n"))                                                                  
y = int(input("enter side Y : \n"))                                                                  
z = int(input("enter side Z : \n"))

if x == y == z:
    print("equilateral")
elif x != y != z:
    print(" scalene")
else:
    print("isosceles")
        

                                                                   
        
    
